public class JTBoolean {

    public static void main(String[] args) {

        //boolean is to check conditions in programming, 1 is for true and 0 is for false.
        // boolean - is primitive data type - can be true and false.
        //Boolean - is Object wrapper class - can be true, false, and null.

        boolean b = true;

        b = !b;

        System.out.println(b);
    }
}
